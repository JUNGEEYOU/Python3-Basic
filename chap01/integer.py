
# 01. 정수
#1) 연산: 덧셈 + , 뺄셈 - , 곱셈 * , 나눗셈 /, 몫 //, 나머지 %
val1 = 3 + 4 
print("1. 덧셈 결과 val1: ",val1)

val2 = 3 - 4
print("2. 뺄셈 결과 val2: ",val2)

val3 = 3 * 4 
print("3. 곱셈 결과 val3: ",val3)


val4 = 3 / 4 
print("4. 나눗셈 결과 val4: ",val4)

val5 = 9 // 4 
print("5. 나눗셈  몫 결과 val5: ",val5)


val6 = 9 % 4 
print("6. 나눗셈 나머지 결과 val6: ",val6)


# 2) 여러 가지 진법으로 수 출력하기 
# - 16진수 
print("hex(0) 값: ",hex(0))
print("hex(255) 값: ",hex(255))
